package com.syntax.class07;

public class WhilevsDoWhile {

	public static void main(String[] args) {
		// hello 5 times
		
	int num=1;
	
	while(num<=5) {
	System.out.println("Hello"); // with while lopp
		
	num++;	
	}  
	
	
	System.out.println("_______USING DO WHILE LOOP___________");	
	int num1=1;
	do {
		System.out.println("Hello");// with do...while loop
		num1++;
		
	}while(num1<=5) ;
		

	}

}
